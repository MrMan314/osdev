.CODE16

.GLOBAL _start

_start:
	JMP MAIN

INTHAND:
	PUSH %AX
	PUSH %BX
	PUSH %ES
	PUSH %DI
	XOR %AX, %AX
	IN $0x60, %AL
	CMP $0x48, %AX
	JE .UP
	CMP $0x4B, %AX
	JE .LEFT
	CMP $0x4D, %AX
	JE .RIGHT
	CMP $0x50, %AX
	JE .DOWN
	JNE .SKIP
	.UP:
	SUBW $320, POS
	JMP .CONT
	.LEFT:
	DECW POS
	JMP .CONT
	.RIGHT:
	INCW POS
	JMP .CONT
	.DOWN:
	ADDW $320, POS
	.CONT:
	MOV $0xA000, %AX
	MOV %AX, %ES
	MOV POS, %DI
	MOV $0x0F, %AL
	STOSB
	.SKIP:
	POP %DI
	POP %ES
	POP %BX
	MOV $0x20, %AL
	OUT %AL, $0x20
	POP %AX

.DONE:
	IRET

MAIN:
	XOR %AX, %AX
	MOV %AX, %DS
	MOV %AX, %ES
	MOV %AX, %FS
	MOV %AX, %GS
	MOV %AX, %SS
	MOVW $0x9000, %SP

	MOV $0x50, %AL
	MOV $24, %BX
	MOV $319, %CX
	MOV $24, %DH
	MOV $199, %DL
	CALL RECT

	CLI
	MOVW $INTHAND, %ES:(0x09*4)
	MOVW $0x0, %ES:(0x09*4+2)
	STI
.LOOP:
	JMP .LOOP

/*
 * Rectangle
 * AL: color
 * BX: start x
 * CX: end x
 * DH: start y
 * DL: end y
 */

RECT:
	PUSH %DI
	PUSH %ES

	PUSH %BX
	MOV $0xA000, %BX
	MOV %BX, %ES
	POP %BX

	PUSH %DX
	MOVZX %DH, %DX

	PUSH %AX
	MOV %DX, %AX
	MULW COLS
	ADDW %BX, %AX
	MOV %AX, %DI
	POP %AX
	POP %DX

	XOR %DH, %DH
	XCHG %DX, %BX
// AL: color
// BX: end y
// CX: end x
// DX: start x
.DRAW:
	STOSB
	PUSH %AX
	PUSH %DX
	MOV %DI, %AX
	DIVW COLS
	CMP %DX, %CX
	JBE .NEXT
	POP %DX
	POP %AX
	JMP .DRAW
.NEXT:
	POP %DX
	POP %AX
	STOSB
	PUSH %AX
	SUBW %CX, %DI
	ADDW %DX, %DI
	ADDW COLS, %DI
	DEC %DI
	MOV %DI, %AX
	PUSH %DX
	XOR %DX, %DX
	DIVW COLS
	CMP %AX, %BX
	JB .NEXT2
	POP %DX
	POP %AX
	JMP .DRAW
.NEXT2:
	POP %DX
	POP %AX
	POP %ES
	POP %DI
	RET

POS: .WORD 0x0000

