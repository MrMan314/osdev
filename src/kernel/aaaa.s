.CODE32
.GLOBL _start
.EXTERN CLEAR
.EXTERN ISR_BEGIN
.EXTERN PS2_INIT
.EXTERN FLOPPY_DETECT
.EXTERN PRINT_HEX_8
.EXTERN PRINT_HEX_16
.EXTERN PRINT_HEX_32
.EXTERN ANALYZE_RSDT
.EXTERN LOCATE_RSDT
.EXTERN LOAD_SDT
.EXTERN INTERRUPT_INIT
.EXTERN SCROLL
.EXTERN PCI_TEST

_start:
	CLI

	CALL CLEAR

	CALL LOCATE_RSDT
	CALL LOAD_SDT
	CALL ANALYZE_RSDT
	CALL FLOPPY_DETECT
	CALL PS2_INIT
	CALL PCI_TEST
	CALL INTERRUPT_INIT

LOOP:
	JMP LOOP

