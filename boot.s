.CODE16

.GLOBAL _start

_start:
//	MOVW $0x0013, %AX
//	INT $0x10

	LJMP $0x0, $MAIN

MAIN:
	XOR %AX, %AX
	MOV %AX, %DS
	MOV %AX, %ES
	MOV %AX, %FS
	MOV %AX, %GS
	MOV %AX, %SS
	MOVW $0x7C00, %SP

	MOV $HELLO, %SI
	CALL PRINT

	XOR %AX, %AX
	MOV %AX, %DS
	CLD

	MOV $0x0240, %AX
	MOV $0x0002, %CX
	XOR %DH, %DH
	XOR %BX, %BX
	MOV %BX, %ES
	MOV $0x1000, %BX
	INT $0x13
	JC .ERR

	LJMP $0x0, $0x1000

	JC .ERR2
PRINT:
	PUSHA
PRINTSTART:
	XORB %BH, %BH
	MOVB $0x0F, %BL
	MOVB $0x0E, %AH
	LODSB
	OR %AL, %AL
	JZ PRINTDONE
	INT $0x10
	JMP PRINTSTART
PRINTDONE:
	POPA
	RET

.ERR2:
	MOV $ERR2, %SI
	CALL PRINT
	JMP .LOOP
.ERR:
	MOV $ERR, %SI
	CALL PRINT
.LOOP:
	JMP .LOOP


BD: .WORD 0x0000
HELLO: .ASCIZ "hello vro\r\n"
ERR: .ASCIZ "help vro\r\n"
ERR2: .ASCIZ "VRO HELP ME\r\n"
.FILL 0x1EB-(.-_start), 0x01, 0x00
